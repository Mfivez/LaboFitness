package be.labofitness.labo_fitness.domain.enums;
import be.labofitness.labo_fitness.domain.entity.Accreditation;

/**
 * Enum representing different types of {@link Accreditation}.
 */
public enum AccreditationType {

    /**
     * Represents a license accreditation.
     */
    LICENCE,

    /**
     * Represents a master's accreditation.
     */
    MASTER,

    /**
     * Represents a PhD accreditation.
     */
    PHD,

    /**
     * Represents a brevet accreditation.
     */
    BREVET
}
